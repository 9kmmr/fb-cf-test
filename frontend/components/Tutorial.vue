<script>
const finageApiKey = process.env.finageApiKey;
const cloudFunctionAPIUrl = process.env.apiUrl;
import InfiniteLoading from 'vue-infinite-loading';
export default {
  name: "NuxtTutorial",
  components: {
    InfiniteLoading,
  },
  props: {
    type: {
      type: String,
      default: "",
    },
    indexPage: {
      type: Boolean,
      default: false,
    },
  },
  data() {
    return {
      data: [],
      coins: [],
      page: 1,
      mobileScreen: false,
    };
  },
  methods: {
    priceStatus(change) {
      if (typeof change === "undefined") {
        return "";
      } else if (change > 0) {
        return "up";
      } else {
        return "down";
      }
    },
    fetCoinsData() {
      return this.$axios
        .$get(`${cloudFunctionAPIUrl}/coins`, {
          json: true,
          gzip: true,
        })
        .then((response) => {
          this.coins = response;
        })
        .catch((error) => {
          console.log(error);
        });
    },
    fetchCoins() {
      return this.$axios
        .$get(
          `https://api.finage.co.uk/list/cryptocurrency?apikey=${finageApiKey}&limit=50`,
          {
            headers: {
              "Content-Type": "application/json",
              "Access-Control-Allow-Origin": "*",
            },
            json: true,
            gzip: true,
          }
        )
        .then((response) => {
          let topCoins = response.results;
          this.data = topCoins;
        })
        .catch((error) => {
          console.log(error);
        });
    },
    lazyLoadCrypto($state){
        this.$axios.$get(`https://api.finage.co.uk/list/cryptocurrency?apikey=${finageApiKey}&limit=50&page=${this.page}`)
        .then(response => {
          if (response.results.length) {
            this.page++;
            this.data = this.data.concat(response.results);            
            $state.loaded();
            if (this.page > 4) {
              $state.complete();
            }
          } else {
            $state.complete();
          }
        })
        .catch(error => {
          console.log(error);
        })
      },
    checkBackground() {
      
      const self = this;
      document.querySelectorAll('.icon').forEach(function(icon) {
            
          if (!icon.style.backgroundImage) {
            const cid = icon.getAttribute("id");
            if (self.coins[cid]) {              
              icon.style.backgroundImage  = ('url("'+self.coins[cid][0].logo+'")');
              console.log('set', cid)
            }
          }
      });
      
    },
  },
  created() {
    if (window.innerWidth < 768) {
      this.mobileScreen = true;
    }
    this.fetCoinsData();
    //this.fetchCoins();
  },
  mounted() {
    this.checkBackground();
    window.addEventListener("scroll", this.checkBackground);
  },
};
</script>

